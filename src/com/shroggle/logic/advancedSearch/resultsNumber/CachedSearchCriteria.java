package com.shroggle.logic.advancedSearch.resultsNumber;

import com.shroggle.entity.OptionDisplayType;

import java.util.List;
import java.util.ArrayList;

/**
 * @author dmitry.solomadin
 */
public class CachedSearchCriteria {

    private String criteria;

    private int optionId;

    private List<Integer> resultingHashCodes = new ArrayList<Integer>();

    private OptionDisplayType optionDisplayType;

    public OptionDisplayType getOptionDisplayType() {
        return optionDisplayType;
    }

    public void setOptionDisplayType(OptionDisplayType optionDisplayType) {
        this.optionDisplayType = optionDisplayType;
    }

    public String getCriteria() {
        return criteria;
    }

    public void setCriteria(String criteria) {
        this.criteria = criteria;
    }

    public int getOptionId() {
        return optionId;
    }

    public void setOptionId(int optionId) {
        this.optionId = optionId;
    }

    public List<Integer> getResultingHashCodes() {
        return resultingHashCodes;
    }

    public void setResultingHashCodes(List<Integer> resultingHashCodes) {
        this.resultingHashCodes = resultingHashCodes;
    }

}
