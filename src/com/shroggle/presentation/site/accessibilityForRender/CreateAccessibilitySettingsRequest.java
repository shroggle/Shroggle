package com.shroggle.presentation.site.accessibilityForRender;

import com.shroggle.entity.AccessibleElementType;
import com.shroggle.entity.AccessibleSettings;
import org.directwebremoting.annotations.DataTransferObject;

/**
 * @author Balakirev Anatoliy
 */
@DataTransferObject
public class CreateAccessibilitySettingsRequest {

    private AccessibleSettings accessibleSettings;

    private boolean saveAccessibilityInCurrentPlace;

    private int elementId;

    private AccessibleElementType elementType;

    private boolean showManageRegistrants;

    private Integer siteId;

    public AccessibleSettings getAccessibleSettings() {
        return accessibleSettings;
    }

    public void setAccessibleSettings(AccessibleSettings accessibleSettings) {
        this.accessibleSettings = accessibleSettings;
    }

    public Integer getSiteId() {
        return siteId;
    }

    public void setSiteId(Integer siteId) {
        this.siteId = siteId;
    }

    public boolean isShowManageRegistrants() {
        return showManageRegistrants;
    }

    public void setShowManageRegistrants(boolean showManageRegistrants) {
        this.showManageRegistrants = showManageRegistrants;
    }

    public int getElementId() {
        return elementId;
    }

    public void setElementId(int elementId) {
        this.elementId = elementId;
    }

    public AccessibleElementType getElementType() {
        return elementType;
    }

    public void setElementType(AccessibleElementType elementType) {
        this.elementType = elementType;
    }

    public boolean isSaveAccessibilityInCurrentPlace() {
        return saveAccessibilityInCurrentPlace;
    }

    public void setSaveAccessibilityInCurrentPlace(boolean saveAccessibilityInCurrentPlace) {
        this.saveAccessibilityInCurrentPlace = saveAccessibilityInCurrentPlace;
    }

}
