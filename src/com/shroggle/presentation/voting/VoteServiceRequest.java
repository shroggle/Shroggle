package com.shroggle.presentation.voting;

import com.shroggle.logic.gallery.VideoRangeEdit;
import org.directwebremoting.annotations.DataTransferObject;
import org.directwebremoting.annotations.RemoteProperty;

/**
 * @author dmitry.solomadin
 */
@DataTransferObject
public class VoteServiceRequest {

    @RemoteProperty
    private int siteId;

    @RemoteProperty
    private int galleryId;

    @RemoteProperty
    private int filledFormId;

    @RemoteProperty
    private int voteValue;

    @RemoteProperty
    private Integer voteId;

    @RemoteProperty
    private VideoRangeEdit videoRangeEdit;

    public VideoRangeEdit getVideoRangeEdit() {
        return videoRangeEdit;
    }

    public void setVideoRangeEdit(VideoRangeEdit videoRangeEdit) {
        this.videoRangeEdit = videoRangeEdit;
    }

    public int getSiteId() {
        return siteId;
    }

    public void setSiteId(int siteId) {
        this.siteId = siteId;
    }

    public int getGalleryId() {
        return galleryId;
    }

    public void setGalleryId(int galleryId) {
        this.galleryId = galleryId;
    }

    public int getFilledFormId() {
        return filledFormId;
    }

    public void setFilledFormId(int filledFormId) {
        this.filledFormId = filledFormId;
    }

    public int getVoteValue() {
        return voteValue;
    }

    public void setVoteValue(int voteValue) {
        this.voteValue = voteValue;
    }

    public Integer getVoteId() {
        return voteId;
    }

    public void setVoteId(Integer voteId) {
        this.voteId = voteId;
    }
}
