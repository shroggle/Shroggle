package com.shroggle.presentation.gallery;



/**
 * @author Balakirev Anatoliy
 */
public class NavigationInnerCell {

    private int formItemId;

    private int column;

    private int position;

    private int sourceImageWidth;

    private int sourceImageHeight;

    private int resizedWidth;

    private int resizedHeight;

    private String align;

    private String value;

    private String alt = "";

    private NavigationCellType navigationCellType;

    public String getAlt() {
        return alt;
    }

    public void setAlt(String alt) {
        this.alt = alt;
    }

    public int getResizedWidth() {
        return resizedWidth;
    }

    public String getResizedWidthAsString() {
        return String.valueOf(resizedWidth);
    }

    public void setResizedWidth(int resizedWidth) {
        this.resizedWidth = resizedWidth;
    }

    public int getResizedHeight() {
        return resizedHeight;
    }

    public String getResizedHeightAsString() {
        return String.valueOf(resizedHeight);
    }

    public void setResizedHeight(int resizedHeight) {
        this.resizedHeight = resizedHeight;
    }

    public int getSourceImageWidth() {
        return sourceImageWidth;
    }

    public void setSourceImageWidth(int sourceImageWidth) {
        this.sourceImageWidth = sourceImageWidth;
    }

    public int getSourceImageHeight() {
        return sourceImageHeight;
    }

    public void setSourceImageHeight(int sourceImageHeight) {
        this.sourceImageHeight = sourceImageHeight;
    }

    public int getPosition() {
        return position;
    }

    public void setPosition(int position) {
        this.position = position;
    }

    public NavigationCellType getNavigationCellType() {
        return navigationCellType;
    }

    public void setNavigationCellType(NavigationCellType navigationCellType) {
        this.navigationCellType = navigationCellType;
    }

    public int getFormItemId() {
        return formItemId;
    }

    public void setFormItemId(int formItemId) {
        this.formItemId = formItemId;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(int column) {
        this.column = column;
    }

    public String getAlign() {
        return align;
    }

    public void setAlign(String align) {
        this.align = align;
    }
}