package com.shroggle.util.mail;

import java.util.regex.Pattern;

/**
 * @author Stasuk Artem
 */
public class MailAddressNoInternetUseValidator implements MailAddressValidator {

    public boolean valid(final String email) {
        return email != null && email.length() > 0 && EMAIL_VALIDATOR.matcher(email.toLowerCase()).replaceFirst("").isEmpty();
    }

    //I changed regexp according to http://tools.ietf.org/html/rfc2822#section-3.4.1
    //The official standard is known as RFC 2822. It describes the syntax that valid email addresses must adhere to.
    //http://www.regular-expressions.info/email.html
    // Tolik
    private static final Pattern EMAIL_VALIDATOR = Pattern.compile("(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])");


}
