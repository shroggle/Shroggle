package com.shroggle.util.config;

/**
 * @author Stasuk Artem
 */
public class ConfigSmtp {

    public String getEmailUpdateApprovalLink() {
        return emailUpdateApprovalLink;
    }

    public void setEmailUpdateApprovalLink(final String emailUpdateApprovalLink) {
        this.emailUpdateApprovalLink = emailUpdateApprovalLink;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(final String login) {
        this.login = login;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(final String password) {
        this.password = password;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(final String url) {
        this.url = url;
    }

    public String getNetworkFrom() {
        return networkFrom;
    }

    public void setNetworkFrom(String networkFrom) {
        this.networkFrom = networkFrom;
    }

    @Override
    public String toString() {
        return "ConfigSmtp {login: " + login + ", url: " + url
                + ", emailUpdateApprovalLink: " + emailUpdateApprovalLink + "}";
    }

    private String login;
    private String networkFrom;
    private String password;
    private String url;
    private String emailUpdateApprovalLink;

}
